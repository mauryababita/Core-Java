package com.nacs.datatype.typecasting;

public class TypeCasting {
	float number;
	char charVriable;

	public void wideningToFloat(float number) {
		this.number = number;
	}

	public void narrowingToChar(char charVriable) {
		this.charVriable = charVriable;
	}

	public float getnumber() {
		return number;

	}

	public char getcharVriable() {
		return charVriable;
	}
}

